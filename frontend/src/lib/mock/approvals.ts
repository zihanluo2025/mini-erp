export function buildPendingApprovalCards(list = pendingApprovalsMock): CardItem[] {
  const totalPending = list.length;
  const submittedToday = list.filter((item) => item.isToday).length;
  const overdue = list.filter((item) => item.isOverdue).length;

  return [
    {
      label: "Total Pending",
      iconSrc: "/icons/kpi-stock.svg",
      value: totalPending,
      trendType: "neutral",
      footer: "All waiting approvals",
    },
    {
      label: "Submitted Today",
      iconSrc: "/icons/kpi-stock.svg",
      value: submittedToday,
      trendType: "positive",
      footer: "Newly submitted today",
    },
    {
      label: "Overdue",
      iconSrc: "/icons/kpi-stock.svg",
      value: overdue,
      trendType: "warning",
      footer: "Need priority review",
    },
  ];
}

